async function updateColors() {
  console.log('Parsing color data...');
  const lines = rawData.split('\n').filter(l => l.trim() !== '');
  const colorsToUpsert = lines.map(line => {
    const parts = line.split(';');
    const code = parts[0].trim();
    const name = parts[1].trim();
    
    // Ignore rows marked with "este no"
    if (line.includes('este no')) return null;

    // Pad code to 4 digits
    const paddedCode = code.padStart(4, '0');
    
    return {
      code_4dig: paddedCode,
      name_color_sap: name
    };
  }).filter(c => c !== null);

  console.log(`Prepared ${colorsToUpsert.length} colors for upsert.`);

  // Batch upsert using exec_sql to bypass RLS
  const batchSize = 50;
  for (let i = 0; i < colorsToUpsert.length; i += batchSize) {
    const batch = colorsToUpsert.slice(i, i + batchSize);
    console.log(`Upserting batch ${Math.floor(i / batchSize) + 1}...`);
    
    const values = batch.map(c => `('${c.code_4dig}', '${c.name_color_sap.replace(/'/g, "''")}')`).join(',');
    const query = `
      INSERT INTO public.colors (code_4dig, name_color_sap)
      VALUES ${values}
      ON CONFLICT (code_4dig) DO UPDATE SET
        name_color_sap = EXCLUDED.name_color_sap;
    `;

    const { error } = await (supabase.rpc as any)('exec_sql', { query_text: query });

    if (error) {
      console.error('Error in batch:', error.message);
    }
  }

  console.log('Update complete!');
}

updateColors();
