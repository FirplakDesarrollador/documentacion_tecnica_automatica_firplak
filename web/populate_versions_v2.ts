async function run() {
    try {
        const lines = data.trim().split('\n');
        console.log(`Processing ${lines.length} versions...`);

        for (const line of lines) {
            const parts = line.split('\t');
            if (parts.length < 2) continue;
            
            const code = parts[0].trim();
            const description = parts[1].trim();

            if (!code) continue;

            let rules = {};
            if (code === 'CHT') rules = { client_name: 'CHILEMAT', private_label_flag: true };
            if (code === 'MRH') rules = { rh: 'RH' };
            if (code === 'MST') rules = { rh: 'NA' };
            if (code === 'CRH') rules = { rh: 'RH' };
            if (code === 'RFM') rules = { rh: 'RH' };

            const q = `
                INSERT INTO public.versions (code, description, automatic_rules)
                VALUES ('${code}', '${description.replace(/'/g, "''")}', '${JSON.stringify(rules)}')
                ON CONFLICT (code) DO UPDATE 
                SET description = EXCLUDED.description, 
                    automatic_rules = EXCLUDED.automatic_rules
            `;
            try {
                await dbQuery(q);
            } catch (err) {
                console.error(`Error inserting code ${code}:`, err.message);
            }
        }

        console.log("Versioning table populated successfully.");
        
        const countRes = await dbQuery("SELECT count(*) as count FROM public.versions");
        console.log("Final count in DB:", countRes[0].count);

    } catch (e) {
        console.error("Failed to populate versions table:", e);
    }
}
run();
